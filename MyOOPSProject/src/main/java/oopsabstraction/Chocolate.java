package oopsabstraction;

/**
 * 
 * @author EKB 
 *         Abstraction : Hiding internal details showing only the required
 *         ones is called abstraction
 * 
 */
public abstract class Chocolate {
	abstract String Silk();

	abstract int Cost_of_Silk();

	abstract String Five_star();

	abstract int Cost_of_five_star();

	abstract String Fruit_N_Nut();

	abstract int Cost_of_fruit_n_nut();

}
